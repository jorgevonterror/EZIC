    public void BuscarTodosAlumnos() {
        TablaAsigEst = (DefaultTableModel) TBAlumnosSinAsig.getModel();
        int a = TablaAsigEst.getRowCount() - 1;
        for (int i = a; i >= 0; i--) {
            TablaAsigEst.removeRow(TablaAsigEst.getRowCount() - 1);
        }
        if (mCE.conectar()) {
            ArrayList mArrayListAlumnos = new ArrayList();
            mArrayListAlumnos = mCE.ConsultarTodosAlumnosSinAsig();
            String[] Datos = null;
            if (mArrayListAlumnos != null) {

                for (int i = 0; i < mArrayListAlumnos.size(); i++) {
                    mEstudiante = (Estudiante) mArrayListAlumnos.get(i);
                    Datos = new String[3];
                    //ID_Asesor = mAsesor.getID_Asesor();
                    Datos[0] = String.valueOf(mEstudiante.getId_Estudiante());
                    Datos[1] = mEstudiante.getNombre();
                    Datos[2] = mEstudiante.getCarrera();
                    TablaAsigEst.addRow(Datos);
                }

            } else {
                //LBL_Mensajero2.setText("No hay puntajes");
            }
            this.TBAlumnosSinAsig = new javax.swing.JTable();
            this.TBAlumnosSinAsig.setModel(TablaAsigEst);
            this.TBAlumnosSinAsig.getColumnModel().getColumn(0).setPreferredWidth(50);
            this.TBAlumnosSinAsig.getColumnModel().getColumn(0).setPreferredWidth(50);
            this.TBAlumnosSinAsig.getColumnModel().getColumn(1).setPreferredWidth(50);
            if (this.TBAlumnosSinAsig.getRowCount() > 0) {
                this.TBAlumnosSinAsig.setRowSelectionInterval(0, 0);
            }

        } else {
            //LBL_Mensajero2.setText("Error al consultar");
        }

        mCE.desconectar();
    }
    public void LlenarComboCarrera() {
        CBCarreraAsig.addItem("Ninguno");

        if (mCE.conectar()) {
            ArrayList mArrayList = new ArrayList();
            mArrayList = mCE.ConsultaCarreraEstudiantes();
            if (mArrayList != null) {

                for (int i = 0; i < mArrayList.size(); i++) {
                    CBCarreraAsig.addItem(mArrayList.get(i).toString());
                }

            } else {
                JOptionPane.showMessageDialog(null, "No tiene Carreras Dadas de Alta");
            }
            mCE.desconectar();
        } else {
            JOptionPane.showMessageDialog(null, "No conectado a la BD");
        }
    }
